export const getBrandProjectBySlug = (slug) => {
  return brandProjects.find(project => project.slug === slug);
};

export const getAdjacentBrandProjects = (currentSlug) => {
  const currentIndex = brandProjects.findIndex(p => p.slug === currentSlug);
  return {
    prev: currentIndex > 0 ? brandProjects[currentIndex - 1] : null,
    next: currentIndex < brandProjects.length - 1 ? brandProjects[currentIndex + 1] : null
  };
};
